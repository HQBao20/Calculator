#include <stdio.h>
#include <stdint.h>

/*******************************************************************************
 * Code
 ******************************************************************************/
uint8_t multi(uint8_t a, uint8_t b)
{
    uint8_t multi = 0;

    multi = a * b;

    return multi;
}

